const STEM_ORDER = ['甲','乙','丙','丁','戊','己','庚','辛','壬','癸']

function seedOffset(dayStem) {
  return (STEM_ORDER.indexOf(dayStem) + 1) % 5
}

function calcPersonSeed(result) {
  const { year, month, day, hour } = result.input
  return (year * 3 + month * 7 + day * 11 + hour * 19) % 5
}

function scoreLevel(score) {
  if (score > 30) return 'high'
  if (score >= 0) return 'mid'
  if (score >= -30) return 'low'
  return 'bad'
}

