/** 起名推荐 */
function calcNameSuggestions(dayStemWx, yongShenWx, count) {
  count = count || 10;
  // 用神五行对应的字库
  const wuxingKey = { '木':'mu','火':'huo','土':'tu','金':'jin','水':'shui' };
  const key = wuxingKey[yongShenWx];
  const pool = key ? (QI_MING_ZI_KU[key] || []) : [];
  const nums = [1, 2, 3]; // 名字字数

  if (pool.length === 0) {
    // 没有匹配的字库，用中性字
    return [{ name: '暂无推荐', reason: '缺少对应五行的字库' }];
  }

  const results = [];
  const used = new Set();

  for (let n = 0; n < count * 2 && results.length < count; n++) {
    // 随机选一个字
    const firstName = pool[Math.floor(Math.random() * pool.length)];
    const secondName = pool[Math.floor(Math.random() * pool.length)];

    // 双名
    const fullName2 = firstName + secondName;
    if (!used.has(fullName2)) {
      used.add(fullName2);
      if (Math.random() > 0.5) {
        results.push({
          name: fullName2,
          firstWx: yongShenWx,
          secondWx: yongShenWx,
          reason: `双字补${yongShenWx}，增强用神力量`
        });
      }
    }

    // 单名
    const fullName1 = firstName;
    if (!used.has(fullName1) && results.length < count) {
      used.add(fullName1);
    }
  }

  return results.slice(0, count);
}
